package in.co.rays;

public class Switch {
 public static void main(String args[]) {
	 int num=1;
	 
	 switch(num) {
	 case 1:{
		 System.out.println("Case 1 is running");
	 break;
	 }
	 
	 case 2:{
		 System.out.println("Case 2 is running");
		 break;
	 }
	  default:{
		  System.out.println("Invalid!!!!");
	  }
	 }
 }
}
