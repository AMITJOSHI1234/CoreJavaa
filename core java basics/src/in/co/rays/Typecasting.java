package in.co.rays;

public class Typecasting {
 public static void main(String args[])
 {
	 double a = 10.15;
	 System.out.println("Value of a:"+a);
	 
	 //typecast
	 
	 System.out.println("Typecast into integer:"+(int)a);
	 
	 int b=10;
	 System.out.println("Typecast into float:" +(float)b);
	 
	 double c=a+b;
	 System.out.println("The value of c is:" +(int)c); 
 }
}
