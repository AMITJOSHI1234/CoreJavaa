package in.co.Encapusulation;

public class encapsulationTask3 {
private String colour;
private int speed;
private int gear;
private String make;

public void setcolour(String colour) {
	this.colour = colour;
}

public String getcolour() {
	return colour;
}

public void setspeed(int speed) {
	this.speed = speed;
}

public int getspeed() {
	return speed;
}
public void setgear(int gear) {
	this.gear = gear;
}

public int getgear() {
	return gear;
}

public void setmake(String make) {
	this.make = make;
}

public String getmake() {
	return make;
}
}
